# Tests: Chunking behaviour for large files
# ---------------------------------------------------------------------------

class TestChunking:

    def _make_large_file_diff(self, num_lines_before: int, num_lines_after: int) -> str:
        """
        Generate a valid diff with precise line counts using real git.
        """
        before = "".join(f"line_{i} = {i}\n" for i in range(num_lines_before))
        after = "".join(f"line_{i} = {i}\n" for i in range(num_lines_after))
        return _make_git_diff(
            files_before={"src/large_file.py": before},
            files_after={"src/large_file.py": after},
        )

    def test_small_file_produces_single_chunk(self):
        diff = self._make_large_file_diff(num_lines_before=3, num_lines_after=50)
        chunks = parse_diff(diff)
        assert len(chunks) == 1
        assert chunks[0].file_path == "src/large_file.py"

    def test_chunk_has_correct_file_path(self):
        diff = self._make_large_file_diff(num_lines_before=3, num_lines_after=50)
        chunks = parse_diff(diff)
        for chunk in chunks:
            assert chunk.file_path == "src/large_file.py"

    def test_chunk_start_line_is_positive(self):
        diff = self._make_large_file_diff(num_lines_before=3, num_lines_after=50)
        chunks = parse_diff(diff)
        for chunk in chunks:
            assert chunk.start_line >= 0
            assert chunk.end_line >= chunk.start_line

    def test_chunk_indices_are_sequential(self):
        """
        When a file produces multiple chunks, chunk_index must be 0, 1, 2...
        """
        diff = self._make_large_file_diff(num_lines_before=3, num_lines_after=50)
        chunks = parse_diff(diff)

        from collections import defaultdict
        by_file: dict = defaultdict(list)
        for chunk in chunks:
            by_file[chunk.file_path].append(chunk)

        for file_chunks in by_file.values():
            indices = sorted(c.chunk_index for c in file_chunks)
            expected = list(range(len(indices)))
            assert indices == expected, f"Non-sequential chunk indices: {indices}"


