# ---------------------------------------------------------------------------
# Diff generation helpers — use real git to produce valid unified diffs
# ---------------------------------------------------------------------------

def _make_git_diff(files_before: dict, files_after: dict) -> str:
    """
    Generate a real unified diff by running git in a temp directory.

    Args:
        files_before: {relative_path: content} for the initial commit
        files_after:  {relative_path: content} for the modified state

    Returns:
        A valid unified diff string matching git's exact output format.
    """
    with tempfile.TemporaryDirectory() as d:
        subprocess.run(['git', 'init', '-q'], cwd=d, check=True)
        subprocess.run(['git', 'config', 'user.email', 't@t.com'], cwd=d, check=True)
        subprocess.run(['git', 'config', 'user.name', 'T'], cwd=d, check=True)

        # Write and commit initial state
        for path, content in files_before.items():
            full_path = os.path.join(d, path)
            os.makedirs(os.path.dirname(full_path), exist_ok=True)
            with open(full_path, 'w') as f:
                f.write(content)

        subprocess.run(['git', 'add', '.'], cwd=d, check=True)
        subprocess.run(['git', 'commit', '-qm', 'init'], cwd=d, check=True)

        # Write modified state (don't commit — diff against working tree)
        for path, content in files_after.items():
            full_path = os.path.join(d, path)
            os.makedirs(os.path.dirname(full_path), exist_ok=True)
            with open(full_path, 'w') as f:
                f.write(content)

        result = subprocess.run(
            ['git', 'diff'],
            cwd=d,
            capture_output=True,
            text=True,
            check=True,
        )
        return result.stdout


