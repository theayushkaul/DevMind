class TestDiffChunkProperties:

    def test_line_count_is_correct(self):
        chunk = DiffChunk(
            file_path="src/api.py",
            start_line=10,
            end_line=20,
            content="+ some change",
            language="python",
        )
        assert chunk.line_count == 11  # 10 to 20 inclusive = 11 lines

    def test_dedup_key_format(self):
        chunk = DiffChunk(
            file_path="src/api.py",
            start_line=1,
            end_line=5,
            content="+ change",
            language="python",
            chunk_index=2,
        )
        assert chunk.dedup_key == "src/api.py:2"

    def test_path_normalises_backslashes(self):
        chunk = DiffChunk(
            file_path="src\\api\\routes.py",
            start_line=1,
            end_line=1,
            content="+line",
            language="python",
        )
        assert chunk.file_path == "src/api/routes.py"
        assert "\\" not in chunk.file_path

    def test_raises_on_negative_start_line(self):
        with pytest.raises(ValueError, match="start_line must be >= 0"):
            DiffChunk(
                file_path="src/api.py",
                start_line=-1,
                end_line=10,
                content="+line",
                language="python",
            )

    def test_raises_when_end_before_start(self):
        with pytest.raises(ValueError, match="end_line"):
            DiffChunk(
                file_path="src/api.py",
                start_line=10,
                end_line=5,
                content="+line",
                language="python",
            )


