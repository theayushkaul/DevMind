# Tests: parse_diff() — the core function
# ---------------------------------------------------------------------------

class TestParseDiff:

    def test_returns_empty_list_when_diff_is_empty(self):
        result = parse_diff("")
        assert result == []

    def test_parses_single_python_file(self, simple_python_diff):
        chunks = parse_diff(simple_python_diff)

        assert len(chunks) == 1
        chunk = chunks[0]
        assert chunk.file_path == "src/api/routes.py"
        assert chunk.language == "python"
        assert chunk.chunk_index == 0

    def test_chunk_contains_diff_content(self, simple_python_diff):
        chunks = parse_diff(simple_python_diff)
        content = chunks[0].content
        assert "create_user" in content

    def test_filters_out_lock_file(self, package_lock_diff):
        chunks = parse_diff(package_lock_diff)
        assert chunks == [], "package-lock.json should be filtered entirely"

    def test_filters_lock_file_but_keeps_python_files(self, multi_file_diff):
        chunks = parse_diff(multi_file_diff)
        file_paths = {c.file_path for c in chunks}
        assert "src/auth.py" in file_paths
        assert "src/models.py" in file_paths
        assert "yarn.lock" not in file_paths, "yarn.lock must be filtered"

    def test_filters_autogenerated_file(self, autogenerated_diff):
        chunks = parse_diff(autogenerated_diff)
        assert chunks == [], "DO NOT EDIT files should be filtered"

    def test_chunk_line_numbers_are_non_negative(self, simple_python_diff):
        chunks = parse_diff(simple_python_diff)
        for chunk in chunks:
            assert chunk.start_line >= 0
            assert chunk.end_line >= chunk.start_line

    def test_multi_file_produces_multiple_chunks(self, multi_file_diff):
        chunks = parse_diff(multi_file_diff)
        # auth.py and models.py — yarn.lock filtered
        assert len(chunks) == 2

    def test_invalid_input_handled_gracefully(self):
        """Garbage that unidiff can't parse should raise or return empty — never crash."""
        try:
            result = parse_diff("this is not a diff at all")
            assert isinstance(result, list)
        except (ValueError, Exception):
            pass  # raising is also acceptable


