class TestRealWorldScenario:

    def test_pr_with_mixed_files_filters_correctly(self, mixed_files_diff):
        chunks = parse_diff(mixed_files_diff)
        reviewed_paths = {c.file_path for c in chunks}

        assert "backend/auth.py" in reviewed_paths
        assert "frontend/api.ts" in reviewed_paths
        assert "proto/generated/user_pb2.py" not in reviewed_paths
        assert "yarn.lock" not in reviewed_paths

    def test_chunk_languages_are_correctly_detected(self, mixed_files_diff):
        chunks = parse_diff(mixed_files_diff)
        lang_by_path = {c.file_path: c.language for c in chunks}

        assert lang_by_path["backend/auth.py"] == "python"
        assert lang_by_path["frontend/api.ts"] == "typescript"

    def test_full_pipeline_produces_valid_chunk_objects(self, mixed_files_diff):
        state = initial_state(
            raw_diff=mixed_files_diff,
            repo_full_name="ayushkaul/devmind",
            pr_number=7,
            head_sha="cafebabe",
        )
        result = run(state)

        assert result.get("error") is None
        for chunk in result["diff_chunks"]:
            assert isinstance(chunk, DiffChunk)
            assert chunk.file_path
            assert chunk.language
            assert chunk.content
            assert chunk.end_line >= chunk.start_line
