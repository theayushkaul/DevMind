class TestRunNode:

    def test_run_returns_diff_chunks_key(self, simple_python_diff):
        state = initial_state(
            raw_diff=simple_python_diff,
            repo_full_name="ayushkaul/test-repo",
            pr_number=1,
            head_sha="abc123",
        )
        result = run(state)
        assert "diff_chunks" in result
        assert isinstance(result["diff_chunks"], list)

    def test_run_does_not_set_error_on_success(self, simple_python_diff):
        state = initial_state(
            raw_diff=simple_python_diff,
            repo_full_name="ayushkaul/test-repo",
            pr_number=1,
            head_sha="abc123",
        )
        result = run(state)
        assert result.get("error") is None

    def test_run_returns_empty_chunks_for_empty_diff(self):
        state = initial_state(
            raw_diff="",
            repo_full_name="ayushkaul/test-repo",
            pr_number=1,
            head_sha="abc123",
        )
        result = run(state)
        assert result["diff_chunks"] == []
        assert result.get("error") is None

    def test_run_does_not_raise_on_any_input(self):
        """
        Fuzz test: run() must never raise regardless of input.
        LangGraph nodes that raise will crash the entire pipeline.
        """
        bad_inputs = [None, "", "   ", "not a diff", "--- /dev/null\n+++ /dev/null\n"]
        for bad_input in bad_inputs:
            state = initial_state(
                raw_diff=bad_input or "",
                repo_full_name="test/repo",
                pr_number=1,
                head_sha="000",
            )
            try:
                result = run(state)
                assert "diff_chunks" in result
            except Exception as exc:
                pytest.fail(
                    f"run() raised {type(exc).__name__} for input {bad_input!r}: {exc}"
                )

    def test_run_populates_chunks_correctly(self, multi_file_diff):
        state = initial_state(
            raw_diff=multi_file_diff,
            repo_full_name="ayushkaul/test-repo",
            pr_number=5,
            head_sha="deadbeef",
        )
        result = run(state)
        assert len(result["diff_chunks"]) == 2
        for chunk in result["diff_chunks"]:
            assert isinstance(chunk, DiffChunk)
            assert chunk.language == "python"


